/*
 * This class demos basic int variables, along with print statements that take multiple args
 */
package Chapter2;

/**
 *
 * @author Adarsh V
 */
public class VariableDemo {
    public static void main(String[] args) {
        int num = 100;
        System.out.println("Value of num is " + num);
        num = num * 2;
        System.out.print("Value of num changes to ");
        System.out.println(num);
    }
}
